class User {
  constructor(
    firstName,
    lastName,
    loginId,
    password,
    email,
    phoneNo
  ) {

    this.firstName = firstName;
    this.lastName = lastName;
    this.loginId = loginId;
    this.password = password;
    this.email = email;
    this.phoneNo = phoneNo;
  }
}

export default User;
